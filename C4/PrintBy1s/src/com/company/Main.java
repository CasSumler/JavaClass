package com.company;

public class Main
{

    public static void main(String[] args)
    {
        int count = 1;

        while (count <= 10)
        {
            System.out.println(count);
            count++;
        }

        count = 10;
        while (count >= 1)
        {
            System.out.println(count);
            count--;
        }
    }
}
